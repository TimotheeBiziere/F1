<paraBTS>
    Le Brevet de Technicien Supérieur aux Services Informatiques aux Organisations (BTS SIO) est un diplôme reconnu par
    l'état de niveau BAC+2 <br>
    Il forme en deux ans des étudiants aux métiers d'administrateur réseau ou de développeur, Ce BTS propose deux
    options : <br>
    <br>
    L'option Solution d'Infrastructure Systèmes et Réseaux (SISR) et L’option Solutions Logicielles et Applications
    Métiers (SLAM) <br>
    <br>
    L’option SISR est destinée aux étudiants qui s’orientent vers les métiers liés à la conception et la maintenance
    d’infrastructures réseaux. <br>
    Assurer la sécurité, la maintenance et l’installation des réseaux et des équipements informatiques font partie des
    principales missions des futurs administrateurs, techniciens ou pilotes d’exploitation. <br>
    Il débouche dans les métiers de :
    <ul>
        <li>Administrateur systèmes et réseaux
        <li>Informaticien support et déploiement
        <li>Pilote d’exploitation
        <li>Support systèmes et réseaux
        <li>Technicien d’infrastructure
        <li>Technicien de production
        <li>Technicien micro et réseau
    </ul>

    <br>
    Tandis que l’option SLAM est destinée aux étudiants qui s’orientent vers les métiers liés à la conception et la
    maintenance de programmes applicatifs. <br>
    Il débouche dans les métiers de : <br>
    <ul>
        <li>Développeur d’applications informatiques
        <li>Développeur informatique
        <li>Analyste d’applications ou d’études
        <li>Analyste programmeur
        <li>Programmeur analyste
        <li>Programmeur d’applications
        <li>Responsable des services applicatifs
        <li>Technicien d’études informatiques
    </ul>
</paraBTS>
